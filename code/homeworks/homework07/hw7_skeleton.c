void nmi_handler(void) __critical __interrupt {
    // we declare them all as static to avoid stack allocation
    static bool busy = false;
    static bool initialized = false;
    static model_t m;
    static model_t *mp = &m;
    static command c;

    if (busy) {
        return; // we cannot afford to do a second interrupt
    }

    busy = true;
    *BUSY_LED = true;
    if (!initialized) {
        model_init(mp);
        initialized = true;
        busy = false;
        *BUSY_LED = false;
        return;
    }
    // CONTROLLER read and MODEL update
    c = controller_read();
    model_update(mp, c);
    view_update(mp);
    busy = false;
    *BUSY_LED = false;
    return;
}

